import java.util.Scanner;
class RightAngle2 
{
	public static void main(String[] args) 
	{
		Scanner sc = new Scanner(System.in);
                                                                             //Loop Control ->
		System.out.println("Enter Number");
		  int n = sc.nextInt();

		  for (int i = 0; i < n ; i ++ )
		  {
			  for (int j = 0; j < n - i ; j++ )
			  {
				  System.out.print("*"+" " );
			  }
			  System.out.println();
		  }

	}
}
